package day02;

public class text7 {
    public static void main(String[] args) {
        add();
        sub();
        mul();
        div();
        remain();
    }
    public static void add() {
        int a=150;
        int b=150;
        System.out.println("x,y的和为：" +(a+b));
    }
    public static void mul() {
        int a1=150;
        int b1=150;
        System.out.println("x,y的差为：" +(a1-b1));
    }
    public static void sub() {
        int a2=150;
        int b2=150;
        System.out.println("x,y的积为：" +(a2*b2));
    }
    public static void div() {
        int a2=150;
        int b2=150;
        System.out.println("x,y的积为：" +(a2*b2));
    }
    public static void remain() {
        int a3=0;
        int b3=0;
        System.out.println("x,y的积为：" +(a3*b3));
    }
}
