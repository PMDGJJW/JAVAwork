package day02;

public class tetx2 {
    public static void main(String[] args) {
        byte a=14;
        byte b=16;
        short a1=45;
        short b1=35;
        byte s1 = (byte)(a+b);
        short s2 = (short)(a1+b1);
        System.out.println("byte类型a和b的和为" +s1);
        System.out.println("short类型a1和b1的和为" +s2);
    }
}
