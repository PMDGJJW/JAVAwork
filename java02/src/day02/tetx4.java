package day02;

public class tetx4 {
    public static void main(String[] args) {
        int a1=10;
        int a2=11;
        int a3=12;
        int a4=13;
        boolean flas=a1%2==0? true:false;
        System.out.println(a1+"是偶数？"+flas);
        boolean flas1=a2%2==0? true:false;
        System.out.println(a2+"是偶数？"+flas1);
        boolean flas2=a3%2!=0? true:false;
        System.out.println(a3+"是偶奇数？"+flas2);
        boolean flas3=a4%2!=0? true:false;
        System.out.println(a4+"是奇数？"+flas3);
    }
}
